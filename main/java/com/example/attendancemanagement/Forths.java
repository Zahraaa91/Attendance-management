package com.example.attendancemanagement;

public class Forths {

    private String Name;
    private Float Dete;
    private  String Sub;
    private  String Dept;
    public Forths() {
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public Float getDete() {
        return Dete;
    }

    public void setDete(Float dete) {
        Dete = dete;
    }

    public String getSub() {
        return Sub;
    }

    public void setSub(String sub) {
        Sub = sub;
    }


    public String getDept() {
        return Dept;
    }

    public void setDept(String dept) {
        Dept = dept;
    }
}
